class Solution: 
    def andOperation(self, a : int, b : int) -> int:
        return a&b

    def longestSubarray(self, numbers : List[int]) -> Tuple[int, List[int]]:
        finalResult = 0 
        andValue = 0
        array = []
        i = 0
        while i < len(numbers):
            main = numbers[i]
            subs = []
            j = i 
            while j < len(numbers) and numbers[j] == main:
                subs.append(numbers[j])
                j += 1 

            if len(subs) == 1:
                tmp = subs[0]
            else:
                tmp = reduce(self.andOperation,subs)
        
            if tmp > andValue:
                andValue = tmp
                finalResult = len(subs)
                array = subs

            i = j

        return (finalResult, array)

